package kutyakfeladat.model;


public class KutyakModel {
    String vizsgalatID;
    String kutyaFajtaID;
    String kutyaNevID;
    String kutyaKora;
    String vizsgalatDatuma;

    public KutyakModel() {
    }

    public String getVizsgalatID() {
        return vizsgalatID;
    }

    public void setVizsgalatID(String vizsgalatID) {
        this.vizsgalatID = vizsgalatID;
    }

    public String getKutyaFajtaID() {
        return kutyaFajtaID;
    }

    public void setKutyaFajtaID(String kutyaFajtaID) {
        this.kutyaFajtaID = kutyaFajtaID;
    }

    public String getKutyaNevID() {
        return kutyaNevID;
    }

    public void setKutyaNevID(String kutyaNevID) {
        this.kutyaNevID = kutyaNevID;
    }

    public String getKutyaKora() {
        return kutyaKora;
    }

    public void setKutyaKora(String kutyaKora) {
        this.kutyaKora = kutyaKora;
    }

    public String getVizsgalatDatuma() {
        return vizsgalatDatuma;
    }

    public void setVizsgalatDatuma(String vizsgalatDatuma) {
        this.vizsgalatDatuma = vizsgalatDatuma;
    }

    

    
}
